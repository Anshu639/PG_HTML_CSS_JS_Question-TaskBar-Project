<?php
$x=5;
$y=6;
$x++; // Post-increment: $x is assigned its original value (5) and then incremented by 1
$y--; // Post-decrement: $y is assigned its original value (6) and then decremented by 1
echo "The value of x is ".$x." and The value of Y is ".$y;
++$x; // Pre-increment: $x is incremented by 1 and then assigned to itself
--$y; // Pre-decrement: $y is decremented by 1 and then assigned to itself
echo "The value of x is ".$x." and The value of Y is ".$y;

?>